#[divan::bench]
fn bench_prove(bencher: divan::Bencher) {
    let chain = chain_data();
    let wallet = wallet_data();

    bencher
        .with_inputs(|| PoCWitnessInputs::from_chain_and_wallet_data(chain.clone(), wallet.clone()))
        .bench_values(|inputs| divan::black_box(prove(inputs).unwrap()));
}
